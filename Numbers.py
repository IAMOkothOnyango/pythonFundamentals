from math import *  # Importing math module

myNum = -5
print (myNum)  # Integer variable (whole number)
print (str(myNum))  # String variable (converted from integer)
print (str(myNum) + " is my favorite number") # String concatenation (combining strings)
print (abs(myNum))  # abs() returns the absolute value of the number passed as an argument
print (pow(3, 2))   # pow() raises the first argument to the power of the second argument
print (max(4, 6))   # max() returns the larger of the two arguments passed
print (min(4, 6))   # min() returns the smaller of the two arguments passed
print (round(3.2))  # round() rounds the number passed as an argument
print (round(3.7))  # round() rounds the number passed as an argument
print (floor(3.7))  # floor() rounds the number passed as an argument down to the nearest integer
print (ceil(3.7))   # ceil() rounds the number passed as an argument up to the nearest integer (opposite of floor())
print (sqrt(36))    # sqrt() returns the square root of the number passed as an argument (square root of 36 is 6)
print (2)   # Integer
print (2.0987)  # Float (decimal number)
print (-2.0987) # Negative number (float)
print (3 + 4.5)
print (3 * (4 + 5)) # Order of operations
print (10 % 3) # Modulus operator