friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"] # List of strings

print(friends) # Prints the entire list

print(friends[3]) # Prints the first item in the list

print(friends[-1]) # Prints the last item in the list

print(friends[1:]) # Prints the second item in the list and everything after it

print(friends[1:3]) # Prints the second item in the list and the third item in the list

friends[1] = "Mike" # Replaces the second item in the list with "Mike"

print(friends[1]) # Prints the second item in the list